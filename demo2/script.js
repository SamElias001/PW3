//  String
let nome = "Samuel";
console.log(nome);
console.log(`O meu nome é ${nome}`);

// Number (número inteiro ou decimal):
let idade = 23;
console.log(idade);
let salario = 1000.01;
console.log(salario);
console.log(`O meu nome é ${nome}, tenho ${idade} anos e atualmente ganho ${salario} reais.`);

// Boolean (true ou false):
let estaLogado = true;
console.log(estaLogado);

// Array (lista de valores):
let frutas = ["Maça","Banana","Uva"];
console.log(`O primeiro valor de array frutas é ${frutas[0]}`);
console.log(`O segundo valor de array frutas é ${frutas[1]}`);
console.log(`O terceiro valor de array frutas é ${frutas[2]}`);

// Obejeto (estrutura de dados chave-valor):
let pessoa = { nome: "Carlos", idade: 30, profissao: "Engenehiro"};
console.log(`O meu nome é ${pessoa.nome}, tenho ${pessoa.idade} anos e sou ${pessoa.profissao}`);